import "./styles.css";

// Create an array to store our particles
var particles: any = [];

// The amount of particles to render
var particleCount = 20;

// The maximum velocity in each direction
var maxVelocity = 2;

// The target frames per second (how often do we want to update / redraw the scene)
var targetFPS = 60;

// Set the dimensions of the canvas as variables so they can be used.
var canvasWidth = 400;
var canvasHeight = 400;

// Create an image object (only need one instance)
var imageObj = new Image();

// Once the image has been downloaded then set the image on all of the particles
imageObj.onload = function() {
    particles.forEach(function(particle: any) {
        particle.setImage(imageObj);
    });
};

// Once the callback is arranged then set the source of the image
imageObj.src = "http://www.pat-cooney.com/images/Smoke.png";

interface Particle {
    x: number;
    y: number;
    xVelocity: number;
    yVelocity: number;
    radius: number;
    context: CanvasRenderingContext2D;
    image?: HTMLImageElement;
}

// A class to create a particle object.
class Particle {
    x: number;
    y: number;
    xVelocity: number;
    yVelocity: number;
    radius: number;
    context: CanvasRenderingContext2D;
    image?: HTMLImageElement;

    constructor(context: CanvasRenderingContext2D) {
        this.x = 0;
        this.y = 0;
        this.xVelocity = 0;
        this.yVelocity = 0;
        this.radius = 5;
        this.context = context;
    }

    draw() {
        if (this.image) {
            this.context.drawImage(this.image, this.x - 128, this.y - 128);
            return;
        }
        this.context.beginPath();
        this.context.arc(this.x, this.y, this.radius, 0, 2 * Math.PI, false);
        this.context.fillStyle = "rgba(225, 211, 255, 0.50)";
        this.context.fill();
        this.context.closePath();
    }

    update() {
        this.x += this.xVelocity;
        this.y += this.yVelocity;
        if (this.x >= canvasWidth) {
            this.xVelocity = -this.xVelocity;
            this.x = canvasWidth;
        } else if (this.x <= 0) {
            this.xVelocity = -this.xVelocity;
            this.x = 0;
        }
        if (this.y >= canvasHeight) {
            this.yVelocity = -this.yVelocity;
            this.y = canvasHeight;
        } else if (this.y <= 0) {
            this.yVelocity = -this.yVelocity;
            this.y = 0;
        }
    }

    setPosition(x: number, y: number) {
        this.x = x;
        this.y = y;
    }

    setVelocity(x: number, y: number) {
        this.xVelocity = x;
        this.yVelocity = y;
    }

    setImage(image: HTMLImageElement) {
        this.image = image;
    }
}

// A function to generate a random number between 2 values
function generateRandom(min: number, max: number) {
    return Math.random() * (max - min) + min;
}

// The canvas context if it is defined.
var context: CanvasRenderingContext2D | null = null;

// Initialise the scene and set the context if possible
function init() {
    var canvas = document.getElementById('myCanvas') as HTMLCanvasElement | null;
    if (canvas && canvas.getContext) {
        // Set the context variable so it can be re-used
        context = canvas.getContext('2d');

        // Create the particles and set their initial positions and velocities
        if (context) {
            for(var i=0; i < particleCount; ++i){
                var particle = new Particle(context);

                // Set the position to be inside the canvas bounds
                particle.setPosition(generateRandom(0, canvasWidth), generateRandom(0, canvasHeight));

                // Set the initial velocity to be either random and either negative or positive
                particle.setVelocity(generateRandom(-maxVelocity, maxVelocity), generateRandom(-maxVelocity, maxVelocity));
                particles.push(particle);
            }
        }
    }
    else {
        alert("Please use a modern browser");
    }
}

// The function to draw the scene
function draw() {
    // Clear the drawing surface and fill it with a black background
    if (context) {
        context.fillStyle = "rgba(0, 0, 0, 0.5)";
        context.fillRect(0, 0, 400, 400);
    }

    // Go through all of the particles and draw them.
    particles.forEach(function(particle: Particle) {
        particle.draw();
    });
}

// Update the scene
function update() {
    particles.forEach(function(particle: Particle) {
        particle.update();
    });
}

// Initialize the scene
init();

// If the context is set then we can draw the scene (if not then the browser does not support canvas)
if (context) {
    setInterval(function() {
        // Update the scene before drawing
        update();

        // Draw the scene
        draw();
    }, 1000 / targetFPS);
}


